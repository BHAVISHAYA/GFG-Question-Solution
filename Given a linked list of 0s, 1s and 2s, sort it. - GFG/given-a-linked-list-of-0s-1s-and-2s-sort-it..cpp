class Solution
{
    public:
    //Function to sort a linked list of 0s, 1s and 2s.
    Node* insert(Node* &head, Node* &tail, int data) {
        Node* newNode = new Node(data);
        if(head == NULL) {
            head = newNode;
            tail = newNode;
        }
        else {
            tail -> next = newNode;
            tail = newNode;
        }
        return head;
    }
    Node* segregate(Node *head) {
        map<int ,int> m;
        Node* temp = head;
        while(temp != NULL) {
            m[temp -> data]++;
            temp = temp -> next;
        }
        Node* Head = NULL;
        Node* Tail = NULL;
        for(auto &value : m) {
            int t = value.second;
            while(t--) {
                Head = insert(Head, Tail, value.first);
            }
        }
        return Head;
    }
};


